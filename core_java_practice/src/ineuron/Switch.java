package ineuron;

public class Switch {
    public static void main(String args[]){
        int num=10;
        switch(num){
            case 100:System.out.println(100);
            break;
            case  20:System.out.println(20);
            break;
            case 123:System.out.println(100);
            break;
            default:System.out.println("no cases matches");

        }

    }
}
